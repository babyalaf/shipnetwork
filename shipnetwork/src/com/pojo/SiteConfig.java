package com.pojo;

public class SiteConfig {

	private String id;
	private String tag;
	private String no;

	private String streamMediaIP;

	private String directIP;
	private String directUserName;
	private String directPassword;
	private String directChannel;

	private String backIP;
	private String backUserName;
	private String backPassword;
	private String backChannel;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getTag() {
		return tag;
	}

	public void setTag(String tag) {
		this.tag = tag;
	}

	public String getNo() {
		return no;
	}

	public void setNo(String no) {
		this.no = no;
	}

	public String getStreamMediaIP() {
		return streamMediaIP;
	}

	public void setStreamMediaIP(String streamMediaIP) {
		this.streamMediaIP = streamMediaIP;
	}

	public String getDirectIP() {
		return directIP;
	}

	public void setDirectIP(String directIP) {
		this.directIP = directIP;
	}

	public String getDirectUserName() {
		return directUserName;
	}

	public void setDirectUserName(String directUserName) {
		this.directUserName = directUserName;
	}

	public String getDirectPassword() {
		return directPassword;
	}

	public void setDirectPassword(String directPassword) {
		this.directPassword = directPassword;
	}

	public String getDirectChannel() {
		return directChannel;
	}

	public void setDirectChannel(String directChannel) {
		this.directChannel = directChannel;
	}

	public String getBackIP() {
		return backIP;
	}

	public void setBackIP(String backIP) {
		this.backIP = backIP;
	}

	public String getBackUserName() {
		return backUserName;
	}

	public void setBackUserName(String backUserName) {
		this.backUserName = backUserName;
	}

	public String getBackPassword() {
		return backPassword;
	}

	public void setBackPassword(String backPassword) {
		this.backPassword = backPassword;
	}

	public String getBackChannel() {
		return backChannel;
	}

	public void setBackChannel(String backChannel) {
		this.backChannel = backChannel;
	}

}
